package PracticeJava;

public class For_Loop_Ex5 {
	public static void main(String[] args) {
		
		
		for (int i = 0; i <=10; i++)
		{
			System.out.println(i);
		}
		for (int i = 2; i <20; i=i+2)
		{
			System.out.println("Even No " + i);
		}
		for (int i=1; i<=21; i=i+2)
		{
			System.out.println("Odd No: "+i);
		}
	}

}
